# SERIALIZER 3: CONTACT MESSAGE - NEW
# Job: Convert ContactMessage Django object → JSON
# Also validates data from React before saving to database
# ==============================================
class ContactMessageSerializer(serializers.ModelSerializer):
    # NEW: Show readable inquiry type instead of 'booking'
    inquiry_type_display = serializers.CharField(source='get_inquiry_type_display', read_only=True)
    
    # ModelSerializer automatically creates validation rules:
    # email field must be valid email format
    # name max_length 100 based on model
    
    class Meta:
        model = ContactMessage  # Link to ContactMessage Django model
        # Fields React sends + fields we send back
        fields = ['id', 'name', 'email', 'phone', 'inquiry_type', 'inquiry_type_display', 
                  'message', 'admin_reply', 'replied_at', 'is_read', 'created_at']
        
        # read_only_fields = React sends these but can't edit them
        # id is auto-generated by database
        # created_at is auto-generated by Django auto_now_add
        read_only_fields = ['id', 'created_at', 'admin_reply', 'replied_at']
        
        # extra_kwargs = add extra rules to fields
        extra_kwargs = {
            'name': {'required': True},  # Must be filled
            'email': {'required': True},  # Must be filled  
            'message': {'required': True},  # Must be filled
            'phone': {'required': False},  # Optional field
        }

# ==============================================
# SERIALIZER 4: USER SIGNUP - NEW
# Job: Handle new user registration
# Validates password match + creates User + ClientProfile
# ==============================================
class SignupSerializer(serializers.ModelSerializer):
    # password2 = extra field to confirm password matches
    password = serializers.CharField(write_only=True, min_length=8, help_text="Min 8 characters")
    password2 = serializers.CharField(write_only=True, help_text="Repeat password")
    phone = serializers.CharField(write_only=True, required=False, allow_blank=True, max_length=20)

    class Meta:
        model = User
        fields = ['id', 'username', 'email', 'password', 'password2', 'first_name', 'last_name', 'phone']
        extra_kwargs = {
            'email': {'required': True},
            'first_name': {'required': False},
            'last_name': {'required': False},
        }

    # Validate that password == password2
    def validate(self, data):
        if data['password'] != data['password2']:
            raise serializers.ValidationError({"password": "Passwords do not match"})
        return data

    # Create user + ClientProfile when signup succeeds
    def create(self, validated_data):
        validated_data.pop('password2')  # remove confirm password
        phone = validated_data.pop('phone', '')  # extract phone for ClientProfile
        
        # create_user hashes password automatically
        user = User.objects.create_user(**validated_data)
        
        # Create linked ClientProfile
        ClientProfile.objects.create(user=user, phone=phone)
        
        return user

