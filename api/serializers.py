from rest_framework import serializers
from django.contrib.auth.models import User
from .models import ModelProfile, ModelPhoto, ContactMessage, ClientProfile
# ^ Import the 3 models we want to convert to JSON + User + ClientProfile for auth

# ==============================================
# SERIALIZER 1: MODEL PHOTO
# Job: Convert ModelPhoto Django object → JSON dict
# Example: {id: 5, image: "http://...", order: 2}
# ==============================================
class ModelPhotoSerializer(serializers.ModelSerializer):
    # ModelSerializer = auto-generates fields based on ModelPhoto model
    # We just tell it which fields to include
    
    class Meta:
        model = ModelPhoto  # Which Django model this serializer uses
        fields = ['id', 'image', 'order']  # Only send these 3 fields to React
        # 'id' = database ID, 'image' = photo URL, 'order' = sort position

# ==============================================
# SERIALIZER 2: MODEL PROFILE  
# Job: Convert ModelProfile + all its photos → JSON
# Example: {id: 1, name: "Queen", photos: [{...}, {...}]}
# ==============================================
class ModelProfileSerializer(serializers.ModelSerializer):
    # Nested Serializer: Include all photos related to this model
    # many=True = because one model has many photos
    # read_only=True = React can read photos but can't create/update them through this endpoint
    photos = ModelPhotoSerializer(many=True, read_only=True)
    
    # ImageField with use_url=True = send full URL like "http://127.0.0.1:8000/media/models/main/queen.jpg"
    # Without use_url, it would send just the file path
    main_image = serializers.ImageField(use_url=True)

    class Meta:
        model = ModelProfile  # Link to ModelProfile Django model
        # List all fields React needs from API
        fields = ['id', 'name', 'profile', 'main_image', 'photos', 'created_at']
        # React uses these to build the cards + gallery modal

